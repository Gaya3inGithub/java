package j8_OOPS;
public class Calculator
{
	public int addition(int a,int b)
	{
		int add = a+b;
		return add;
	}
	public int subraction(int a,int b)
	{
		int sub = a-b;
		return sub;
	}
	public int multiplication(int a,int b)
	{
		int mul = a*b;
		return mul;
	}
	public int division(int a,int b)
	{
		int div= a/b;
		return div;
	}

}
